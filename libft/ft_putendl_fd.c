#include "libft.h"

int	ft_putendl_fd(char *s, int fd, int flag)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
	ft_putchar_fd('\n', fd);
	if (s && flag == FREE_ON)
		free(s);
	return (0);
}
